class BitShift 
{
    public static void main(String[] args) 
	{
		int num=5;
        int leftShift=num<<1;
		int rightShift=num>>1;

        System.out.println("Original Number:"+num);
        System.out.println("Left Shift:"+leftShift);
        System.out.println("Right Shift:"+rightShift);
    }
}